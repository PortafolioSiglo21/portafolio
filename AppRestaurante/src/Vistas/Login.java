    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //'" + user + "'
        //'" + pass + "'"
        user = txtUserLogin.getText().trim().toLowerCase();
        pass = txtPassLogin.getText().trim().toLowerCase();
        if(!user.equals("") || !pass.equals("")){
             try{
                 Connection con = Conexion.getConnection();
                 PreparedStatement pst = con.prepareStatement(
                 "SELECT ID_CARGO, STATUS_EMPLEADO FROM EMPLEADOS WHERE USUARIO = ?  AND PASS = ?");
                 pst.setString(1, user);
                 pst.setString(2, pass);
                 ResultSet rs = pst.executeQuery();
                 if(rs.next()){
                        
                     //pst.setString(1, user);
                     //pst.setString(2, pass);
                     int ID_CARGO = rs.getInt("ID_CARGO");
                     String STATUS_EMPLEADO = rs.getString("STATUS_EMPLEADO");
                     
                     if(ID_CARGO== 1  && STATUS_EMPLEADO.equalsIgnoreCase("ACTIVO")){
                        barra.setVisible(true);
                        cont=-1;
                        barra.setValue(0);
                        barra.setStringPainted(true);
                        timer=new Timer(TWO_SECOND, new TimerListener());
                        timer.start();
                        if(barra.getValue()==100){
                            this.dispose();
                        }
                        //dispose();
                        //new InicioAdmin().setVisible(true);
                        
                        }else if(ID_CARGO== 6  && STATUS_EMPLEADO.equalsIgnoreCase("ACTIVO")){
                          dispose();
                          new IncioBodeguero().setVisible(true);
                        }else if(ID_CARGO== 2  && STATUS_EMPLEADO.equalsIgnoreCase("ACTIVO")){
                           
                          dispose();
                          new InicioFinanzas().setVisible(true);
                        }
                 }else{
                     JOptionPane.showMessageDialog(null, "Datos de Acceso Incorrectos");
                     System.err.println("Datos de Acceso Incorrectos");
                     txtUserLogin.setText("");
                     txtPassLogin.setText("");
                     con.close();
                }
                 
             }catch(SQLException | HeadlessException e){
                 System.err.println("Error "+ e);
                 JOptionPane.showMessageDialog(null, "Error al conectarse, consulte al administrador");
             }
             
        }else{
            JOptionPane.showMessageDialog(null, "Disculpa,debes llenar Los campos Vacios");  
        }
    }//GEN-LAST:event_btnLoginActionPerformed

